"""Translate a baseline audio evidence packet into O/M/E candidate fields.

This is the first minimal mechanism-to-OME adapter for MSSL.
It reads `audio_evidence_packet.json` generated by
`scripts/run_librosa_baseline_evidence.py` and writes:

- ome_mapping_packet.json

The output is not a listening report and does not create confirmed auditory
objects. It only creates auditable O-space, M-domain, E-space, and object-input
candidates with explicit support and over-interpretation boundaries.
"""

from __future__ import annotations

import argparse
import json
import math
from pathlib import Path
from typing import Any


DEFAULT_OUTPUT_NAME = "ome_mapping_packet.json"
EPSILON = 1e-12


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Translate baseline audio evidence into minimal O/M/E candidates."
    )
    parser.add_argument(
        "--input",
        required=True,
        help="Path to audio_evidence_packet.json generated by the librosa baseline adapter.",
    )
    parser.add_argument(
        "--output",
        default=None,
        help="Optional output path. Default: write ome_mapping_packet.json next to input.",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    input_path = Path(args.input)
    if not input_path.exists():
        raise FileNotFoundError(f"Evidence packet not found: {input_path}")

    evidence_packet = json.loads(input_path.read_text(encoding="utf-8"))
    mapping_packet = build_mapping_packet(evidence_packet, input_path)

    output_path = Path(args.output) if args.output else input_path.with_name(DEFAULT_OUTPUT_NAME)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(json.dumps(mapping_packet, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Wrote {output_path}")


def build_mapping_packet(evidence_packet: dict[str, Any], input_path: Path) -> dict[str, Any]:
    features = evidence_packet.get("features", {})
    temporal = features.get("temporal_evidence", {})
    spectral = features.get("spectral_evidence", {})
    rhythm = features.get("rhythm_evidence", {})
    harmonic = features.get("harmonic_evidence", {})
    mel_mfcc = features.get("mel_mfcc_evidence", {})
    stereo = features.get("stereo_proxy_evidence", {})

    o_space = [
        spectral_brightness_candidate(spectral),
        spectral_density_candidate(spectral),
        harmonic_contour_candidate(harmonic),
        timbre_shape_candidate(mel_mfcc),
    ]

    m_domain = [
        pressure_transfer_candidate(temporal),
        activity_continuity_candidate(temporal),
        pulse_transfer_candidate(rhythm),
    ]

    e_space = [
        perceived_pressure_candidate(temporal),
        receiver_width_candidate(stereo),
        receiver_balance_candidate(stereo),
        receiver_spread_candidate(stereo),
        upper_tendency_candidate(spectral),
    ]

    object_inputs = [
        rhythmic_object_input(rhythm),
        transient_activity_input(rhythm, temporal),
        harmonic_layer_input(harmonic),
        texture_mass_input(spectral, mel_mfcc),
    ]

    mapping_packet = {
        "schema": "mssl_mechanism_to_ome_mapping_v0_1",
        "mapper": {
            "name": "mechanism_to_ome_baseline",
            "role": "minimal baseline mapper",
            "status": "candidate mapping only; not auditory object tracking and not listening report",
        },
        "input_packet": {
            "path": str(input_path),
            "schema": evidence_packet.get("schema"),
            "adapter": evidence_packet.get("adapter", {}).get("name"),
            "filename": evidence_packet.get("input", {}).get("filename"),
            "sample_rate": evidence_packet.get("input", {}).get("sample_rate"),
            "analyzed_duration_seconds": evidence_packet.get("input", {}).get("analyzed_duration_seconds"),
        },
        "policy": {
            "not_a_listening_report": True,
            "not_confirmed_object_identity": True,
            "not_physical_source_localization": True,
            "not_genre_or_taste_judgment": True,
            "next_required_step": "object candidate building and temporal-spatial object tracking",
        },
        "ome_candidates": {
            "O_space": o_space,
            "M_domain": m_domain,
            "E_space": e_space,
            "object_candidate_inputs": object_inputs,
        },
        "mapping_rule_index": mapping_rule_index(),
        "audit": {
            "feature_warnings_from_input": evidence_packet.get("feature_warnings", []),
            "global_cannot_prove": [
                "cannot prove whether the song is good",
                "cannot prove genre, emotion, cultural meaning, or review language",
                "cannot prove real physical source location from stereo proxy fields",
                "cannot identify singer, speaker, lyric, or instrument identity",
                "cannot replace human annotation or calibration",
            ],
        },
    }
    return sanitize(mapping_packet)


def spectral_brightness_candidate(spectral: dict[str, Any]) -> dict[str, Any]:
    centroid = stat_mean(spectral, "spectral_centroid_hz")
    rolloff = stat_mean(spectral, "spectral_rolloff_hz")
    score = bounded_score(relative_value(centroid, low=800.0, high=5000.0), relative_value(rolloff, low=2000.0, high=12000.0))
    return candidate(
        field="source_side_spectral_brightness_candidate",
        layer="O-space",
        value=score,
        confidence=confidence_from_values(centroid, rolloff),
        can_support=[
            "modeled source-side upper-frequency energy tendency",
            "brightness or upper-band presence as audio evidence",
        ],
        cannot_prove=[
            "cannot prove instrument identity",
            "cannot prove real source location",
            "cannot directly become report language such as bright, cold, sharp, or beautiful",
        ],
        source_features=["spectral_centroid_hz.mean", "spectral_rolloff_hz.mean"],
    )


