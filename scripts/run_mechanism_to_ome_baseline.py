def candidate(
    field: str,
    layer: str,
    value: float | None,
    confidence: float,
    can_support: list[str],
    cannot_prove: list[str],
    source_features: list[str],
    extra: dict[str, Any] | None = None,
) -> dict[str, Any]:
    payload = {
        "field": field,
        "layer": layer,
        "value": safe_float(value),
        "confidence": safe_float(confidence),
        "source_features": source_features,
        "can_support": can_support,
        "cannot_prove": cannot_prove,
    }
    if extra:
        payload.update(extra)
    return payload


def stat_mean(container: dict[str, Any], key: str) -> float | None:
    return stat_value(container, key, "mean")


def stat_value(container: dict[str, Any], key: str, stat_key: str) -> float | None:
    item = container.get(key)
    if not isinstance(item, dict):
        return None
    return as_float(item.get(stat_key))


def list_stat_mean(items: Any, index: int) -> float | None:
    if not isinstance(items, list) or index >= len(items):
        return None
    item = items[index]
    if not isinstance(item, dict):
        return None
    return as_float(item.get("mean"))


def abs_or_none(value: float | None) -> float | None:
    return None if value is None else abs(value)


def relative_value(value: float | None, low: float, high: float) -> float | None:
    if value is None:
        return None
    if high <= low:
        return None
    return clamp01((value - low) / (high - low))


def bounded_score(*values: float | None) -> float | None:
    valid = [value for value in values if value is not None and math.isfinite(value)]
    if not valid:
        return None
    return clamp01(sum(valid) / len(valid))


def confidence_from_values(*values: Any) -> float:
    if not values:
        return 0.0
    valid_count = sum(1 for value in values if as_float(value) is not None)
    return clamp01(valid_count / len(values))


def clamp01(value: float | None) -> float | None:
    if value is None or not math.isfinite(value):
        return None
    return max(0.0, min(1.0, float(value)))


def as_float(value: Any) -> float | None:
    if value is None:
        return None
    try:
        result = float(value)
    except (TypeError, ValueError):
        return None
    if not math.isfinite(result):
        return None
    return result


def safe_float(value: Any) -> float | None:
    result = as_float(value)
    if result is None:
        return None
    return round(result, 8)


def sanitize(value: Any) -> Any:
    if isinstance(value, dict):
        return {str(key): sanitize(item) for key, item in value.items()}
    if isinstance(value, list):
        return [sanitize(item) for item in value]
    if isinstance(value, tuple):
        return [sanitize(item) for item in value]
    if isinstance(value, float):
        return safe_float(value)
    return value


