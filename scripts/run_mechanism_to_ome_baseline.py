def receiver_spread_candidate(stereo: dict[str, Any]) -> dict[str, Any]:
    corr = as_float(stereo.get("phase_correlation_proxy"))
    side_mid = as_float(stereo.get("side_to_mid_ratio"))
    decorrelation = None if corr is None else 1.0 - ((corr + 1.0) / 2.0)
    score = bounded_score(decorrelation, relative_value(side_mid, low=0.05, high=1.2))
    return candidate(
        field="receiver_side_spread_candidate",
        layer="E-space",
        value=score,
        confidence=confidence_from_values(corr, side_mid) if stereo.get("available") else 0.0,
        can_support=[
            "receiver-side diffusion or spread candidate",
            "possible non-centered layer evidence",
        ],
        cannot_prove=[
            "cannot prove reverb source or room geometry",
            "cannot prove physical envelopment without further evidence",
        ],
        source_features=["phase_correlation_proxy", "side_to_mid_ratio"],
    )


